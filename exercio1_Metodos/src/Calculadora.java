public class Calculadora {
	
	public static void soma  (double num1, double num2 ) {
		double resultado = numero1 + numero2;
		System.out.println("A soma de " + numero1 + " e de " + numero2 + " e de " + resultado);
		
	}
	
	public static void subtracao ( double numero1, double numero2) {
		double resultado = numero1 - numero2;
		System.out.println("A subtacao de " + numero1 + " e de " + numero2 + " e de " + resultado);
	}
	
	public static void divicao ( double numero1, double numero2) {
		double resultado = numero1 / numero2;
		System.out.println("A divicao de " + numero1 + " e de " + numero2 + " e de " + resultado);
		
	}
	
	public static void multiplicacao ( double numero1, double numero2) {
		double resultado = numero1 * numero2;
		System.out.println("A Multiplicacao de " + numero1 + " e de " + numero2 + " e de "+ resultado);
	}
}